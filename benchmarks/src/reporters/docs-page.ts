import { writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";
import type { ResultRow } from "../types.js";
import { renderMarkdown } from "./markdown.js";

/**
 * Writes the benchmark report into the docs site as a generated page.
 * Path is configurable via --docs-out= or BENCH_DOCS_OUT env var.
 */
export function writeDocsPage(path: string, rows: ResultRow[]): void {
	mkdirSync(dirname(path), { recursive: true });
	const content = `<!-- Generated by @storely/benchmarks. Do not edit by hand. -->\n\n${renderMarkdown(rows)}`;
	writeFileSync(path, content);
}
